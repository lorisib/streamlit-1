num1 = float(input("Please enter a number"))
num2 = float(input("Please enter a second  number"))
operation = input("plz enter operacion(+ ,- ,/ ,*)")

if operation=='+':
    print(num1 + num2)
elif operation =='-':
    print(num1 - num2)
elif operation==':':
    print(num1 / num2)
elif operation=='*':
    print(num1 + num2)
else:
    print('no operation')



